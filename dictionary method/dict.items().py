vehicles = {
    "car": 2,
    "bike": 4,
    "bus": 1
}

for vehicle, quantity in vehicles.items():
    print(f"There are {quantity} {vehicle}s.")
    animals = {
        "dog": 3,
        "cat": 5,
        "bird": 8
    }

    for animal, count in animals.items():
        print(f"There are {count} {animal}s.")